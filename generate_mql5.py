def generate_all(top3_configs: list):
    """Generate tất cả EAs + README từ top3 configs list."""
    OUTPUT_DIR.mkdir(exist_ok=True)
    print(f"\n{'='*60}")
    print(f"  🖊️  Generating MQL5 EAs → {OUTPUT_DIR}")
    print(f"{'='*60}")
