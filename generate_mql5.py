"""
MQL5 EA Generator — Ichimoku Rule-based Strategy.
===================================================
Đọc models/top3_configs.json → generate IchiRule_Top{N}.mq5

Mỗi EA gồm:
- Ichimoku cloud break + optional TK cross
- EMA filter (optional)
- SL/TP cố định + optional trailing stop
- Money management: fixed lot, max DD guard
- Commentary chi tiết (entry reason, expected metrics)
"""

import json
